#import "IMUUID.h"
#import "IMKeyChainStore.h"
#import "sys/utsname.h"
#import "YGUtilities.h"
@implementation IMUUID

+(NSString *)getUUID
{
    NSString * strUUID = (NSString *)[IMKeyChainStore load:[IMUUID getBundleID]];
    
    //首次执行该方法时，uuid为空
    if ([strUUID isEqualToString:@""] || !strUUID)
    {
        //生成一个uuid的方法
        CFUUIDRef uuidRef = CFUUIDCreate(kCFAllocatorDefault);
        
        strUUID = (NSString *)CFBridgingRelease(CFUUIDCreateString (kCFAllocatorDefault,uuidRef));
        
        //将该uuid保存到keychain
        [IMKeyChainStore save:@"com.manjinba.im" data:strUUID];
        
    }
    return strUUID;
}


